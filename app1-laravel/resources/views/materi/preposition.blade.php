  <b>Prepositions (Preposisi)</b>

Prepositions adalah kata-kata yang digunakan untuk menunjukkan hubungan antara kata benda atau kata ganti dengan kata lain dalam sebuah kalimat. Prepositions seringkali menunjukkan hubungan waktu, tempat, arah, atau cara.<br>

<br><b>1. Prepositions of Place (Preposisi Tempat)</b>
Prepositions of place digunakan untuk menunjukkan lokasi atau posisi.

<br>-  In : di dalam <br>
- Contoh: The book is in the bag. <br>
-  On : di atas (menyentuh permukaan) <br>
- Contoh: The pen is on the table. <br>
-  At : di (tempat tertentu) <br>
- Contoh: She is at the door. <br>

<br><b>2. Prepositions of Time (Preposisi Waktu)</b>
Prepositions of time digunakan untuk menunjukkan waktu.

<br>-  In : untuk bulan, tahun, abad, atau periode waktu tertentu <br>
- Contoh: She was born in April. / We will visit them in 2022. <br>
-  On : untuk hari dan tanggal <br>
- Contoh: The meeting is on Monday. / The party is on 15th May. <br>
-  At : untuk waktu tertentu <br>
- Contoh: The class starts at 8 AM. <br>

<br><b>3. Prepositions of Direction (Preposisi Arah)</b>
Prepositions of direction digunakan untuk menunjukkan arah atau tujuan.

<br>-  To : ke (menuju suatu tempat) <br>
- Contoh: She is going to the market. <br>
-  Into : ke dalam <br>
- Contoh: He jumped into the pool. <br>
-  Out of : keluar dari <br>
- Contoh: She walked out of the room. <br>

<b>4. Prepositions of Manner (Preposisi Cara)</b>
Prepositions of manner digunakan untuk menunjukkan cara sesuatu dilakukan.

<br>-  By : dengan (menunjukkan cara atau alat) <br>
- Contoh: She traveled by bus. <br>
-  With : dengan (menunjukkan alat atau cara) <br>
- Contoh: He cut the paper with scissors. <br>
-  Like : seperti <br>
- Contoh: She sings like a professional singer. <br>

<br><b>5. Common Prepositional Phrases</b>
Beberapa frasa preposisional umum yang sering digunakan dalam kalimat.

<br>-  At the moment : saat ini <br>
- Contoh: She is busy at the moment. <br>
-  In the morning/afternoon/evening : pada pagi/siang/sore hari <br>
- Contoh: We will meet in the morning. <br>
-  On time : tepat waktu <br>
- Contoh: He arrived on time.
