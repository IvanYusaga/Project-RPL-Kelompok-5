  <b>Present Perfect Tense</b>
Present Perfect Tense digunakan untuk menyatakan suatu tindakan atau keadaan yang telah dimulai di masa lalu dan masih berlanjut hingga sekarang atau memiliki efek atau hasil di masa kini. Bentuk ini juga sering digunakan untuk pengalaman hidup.<br>

<br> Rumus: 
- Positive: S + have/has + past participle (V3)
- Negative: S + have/has + not + past participle (V3)
- Question: Have/has + S + past participle (V3)?<br>

Contoh: 
- Positive: She has visited Paris.
- Negative: They have not finished their homework.
- Question: Have you ever tried sushi?<br>

<br><b>Present Continuous Tense</b>
Present Continuous Tense digunakan untuk menyatakan tindakan yang sedang berlangsung pada saat berbicara atau untuk tindakan yang telah direncanakan akan terjadi di masa depan.<br>

<br> Rumus: 
- Positive: S + am/is/are + V-ing
- Negative: S + am/is/are + not + V-ing
- Question: Am/is/are + S + V-ing?<br>

Contoh: 
- Positive: I am reading a book now.
- Negative: He is not watching TV.
- Question: Are they playing soccer?<br>

<br><b>Past Continuous Tense</b>
Past Continuous Tense digunakan untuk menyatakan tindakan yang sedang berlangsung pada waktu tertentu di masa lalu atau ketika ada tindakan lain yang menginterupsi.<br>

<br> Rumus: 
- Positive: S + was/were + V-ing
- Negative: S + was/were + not + V-ing
- Question: Was/were + S + V-ing?<br>

Contoh: 
- Positive: She was cooking dinner at 7 PM.
- Negative: They were not sleeping when I called.
- Question: Were you studying at midnight?<br>

<br><b>Imperative Sentence</b>
Imperative Sentence digunakan untuk memberikan perintah, instruksi, atau permintaan.<br>

<br> Rumus: 
- Positive: V1 (base form)
- Negative: Do not + V1 (base form)<br>

Contoh: 
- Positive: Close the door.
- Negative: Do not touch the painting.<br>

<br><b>Conjunction</b>
Conjunction (kata hubung) digunakan untuk menghubungkan kata, frasa, atau klausa dalam kalimat. Terdapat beberapa jenis conjunction:<br>

<br> 1. Coordinating Conjunctions: 
Menghubungkan kata, frasa, atau klausa yang memiliki derajat yang sama.
- For, And, Nor, But, Or, Yet, So (FANBOYS)<br>

Contoh: 
- She likes apples and oranges.
- He wanted to go, but he was too tired.<br>

<br> 2. Subordinating Conjunctions: 
Menghubungkan klausa utama dengan klausa subordinat (klausa yang tidak bisa berdiri sendiri).
- Because, Although, Since, Unless, While, etc.<br>

Contoh: 
- She went to bed early because she was tired.
- Although it was raining, they went for a walk.<br>

<br> 3. Correlative Conjunctions: 
Berpasangan untuk menghubungkan kata atau kelompok kata yang sama pentingnya.
- Either...or, Neither...nor, Both...and, Not only...but also<br>

Contoh: 
- Both the teacher and the students were excited.
- Either you finish your homework or you cannot go out.<br>

