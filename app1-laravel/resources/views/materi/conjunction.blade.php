    <b>Conjunctions (Kata Hubung)</b><br>

    Conjunctions atau kata hubung adalah kata-kata yang digunakan untuk menghubungkan kata, frasa, atau klausa dalam sebuah kalimat. Kata hubung berfungsi untuk membuat kalimat lebih bervariasi dan kompleks.<br>
    <br><b>1. Coordinating Conjunctions (Kata Hubung Koordinatif)</b>
    
    <br>Coordinating conjunctions digunakan untuk menghubungkan kata, frasa, atau klausa yang setara atau memiliki derajat yang sama. Ada beberapa coordinating conjunctions yang bisa diingat dengan akronim FANBOYS:
    <br>-   For  : untuk (menunjukkan alasan) <br>
    Contoh: I stayed home, for it was raining outside.

    <br>-   And  : dan (menambahkan informasi) <br>
        Contoh: She likes apples and oranges.
    
    <br>-   Nor  : juga tidak (menambahkan informasi negatif) <br>
        Contoh: He doesn't like coffee, nor does he like tea.<br>
    
    <br><b>2. Subordinating Conjunctions (Kata Hubung Subordinatif)</b>

    <br>Subordinating conjunctions digunakan untuk menghubungkan klausa utama (induk kalimat) dengan klausa subordinat (anak kalimat). Klausa subordinat adalah klausa yang tidak dapat berdiri sendiri sebagai kalimat lengkap.
    Beberapa subordinating conjunctions yang umum digunakan adalah:
    
    
        <br>-   Because  : karena <br>
        Contoh: She went to bed early because she was tired.
    
        <br>-   Although/Though  : meskipun <br>
        Contoh: Although it was raining, they went for a walk.
    
        <br>- Since : sejak, karena <br>
        Contoh: I have known her since we were children. <br>
    
    
    
    <br><b> 3. Correlative Conjunctions (Kata Hubung Korelatif)</b>
    <br>Correlative conjunctions adalah pasangan kata hubung yang bekerja bersama untuk menghubungkan kata, frasa, atau klausa yang setara. Beberapa correlative conjunctions yang umum digunakan adalah:
    
        <br>- Either...or : baik...atau <br>
        Contoh: You can either call me or send an email.
        
        <br>- Neither...nor : tidak...tidak juga <br>
        Contoh: He is neither a doctor nor a nurse.
        
        <br>- Both...and : baik...dan juga <br>
        Contoh: Both the teacher and the students were happy.<br>
    
    <br><b>Contoh Penggunaan dalam Kalimat </b>
    <br>1.   Coordinating Conjunctions  :
        - I want to buy a new book, but I don't have enough money.
        - She is studying hard, for she wants to get good grades.
    <br>2.   Subordinating Conjunctions  :
        - I will help you with your homework if I have time.
        - Although it was late, they continued working.
    <br>3.   Correlative Conjunctions  :
        - You must decide whether to join the team or not.
        - Both the movie and the book were amazing.
    <p>
    Dengan memahami dan mempraktikkan penggunaan conjunctions ini, siswa dapat membuat kalimat yang lebih kaya dan beragam, serta meningkatkan kemampuan berbahasa Inggris mereka. Semoga bermanfaat!
